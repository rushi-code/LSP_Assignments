#include<stdio.h>
#include<sys/resource.h>

int main()
{
    int Ret = 0;

    Ret = nice(0);
    printf("Current priority of process is %d\n",Ret);

    Ret = nice(5);
    printf("updated priority of current process is %d\n",Ret);
    
    return 0;
}
